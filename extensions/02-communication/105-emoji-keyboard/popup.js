class EmojiKeyboard {
  constructor() {
    this.currentCategory = 'recent';
    this.recentEmojis = [];
    this.initElements();
    this.bindEvents();
    this.loadData();
  }

  initElements() {
    this.searchEl = document.getElementById('searchEmoji');
    this.gridEl = document.getElementById('emojiGrid');
    this.statusEl = document.getElementById('status');
    this.catBtns = document.querySelectorAll('.cat-btn');
  }

  bindEvents() {
    this.catBtns.forEach(btn => {
      btn.addEventListener('click', () => {
        this.catBtns.forEach(b => b.classList.remove('active'));
        btn.classList.add('active');
        this.currentCategory = btn.dataset.category;
        this.searchEl.value = '';
        this.render();
      });
    });

    this.searchEl.addEventListener('input', () => this.handleSearch());
  }

  async loadData() {
    const result = await chrome.storage.local.get('recentEmojis');
    if (result.recentEmojis) {
      this.recentEmojis = result.recentEmojis;
    }
    this.render();
  }

  async saveData() {
    await chrome.storage.local.set({ recentEmojis: this.recentEmojis });
  }

  handleSearch() {
    const query = this.searchEl.value.toLowerCase();
    if (!query) {
      this.render();
      return;
    }

    const allEmojis = Object.values(EMOJIS).flat();
    this.renderEmojis(allEmojis);
  }

  render() {
    let emojis = [];
    if (this.currentCategory === 'recent') {
      emojis = this.recentEmojis;
    } else {
      emojis = EMOJIS[this.currentCategory] || [];
    }
    this.renderEmojis(emojis);
  }

  renderEmojis(emojis) {
    if (emojis.length === 0) {
      this.gridEl.innerHTML = '<div class="empty-state">No emojis found</div>';
      return;
    }

    this.gridEl.innerHTML = emojis.map(emoji =>
      `<button class="emoji-btn" data-emoji="${emoji}">${emoji}</button>`
    ).join('');

    this.gridEl.querySelectorAll('.emoji-btn').forEach(btn => {
      btn.addEventListener('click', () => this.copyEmoji(btn.dataset.emoji));
    });
  }

  async copyEmoji(emoji) {
    await navigator.clipboard.writeText(emoji);

    // Add to recent
    this.recentEmojis = this.recentEmojis.filter(e => e !== emoji);
    this.recentEmojis.unshift(emoji);
    if (this.recentEmojis.length > 32) {
      this.recentEmojis.pop();
    }
    this.saveData();

    // Update status
    this.statusEl.textContent = `Copied ${emoji}`;
    this.statusEl.classList.add('copied');
    setTimeout(() => {
      this.statusEl.textContent = 'Click an emoji to copy';
      this.statusEl.classList.remove('copied');
    }, 1500);
  }
}

document.addEventListener('DOMContentLoaded', () => new EmojiKeyboard());
